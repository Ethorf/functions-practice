class Node {
  constructor(value) {
    this.value = value;
    this.next = null;
  }
}

class Queue {
  constructor() {
    this.first = null;
    this.last = null;
    this.size = 0;
  }
  enqueue(val) {
    var newNode = new Node(val);
    if (!this.first) {
      this.first = newNode;
      this.last = newNode;
    } else {
      this.last.next = newNode;
      this.last = newNode;
    }
    return ++this.size;
  }

  dequeue() {
    if (!this.first) return null;

    var temp = this.first;
    if (this.first === this.last) {
      this.last = null;
    }
    this.first = this.first.next;
    this.size--;
    return temp.value;
  }
}

const bfs = (tree) => {
  let q = new Queue();
  let returnArr = [];
  q.enqueue(tree.root);
  while (q.length) {
    let current = q.dequeue();
    returnArr.push(current);
    if (current.right) {
      q.enqueue(current.right);
    }
  }
  return returnArr;
};
